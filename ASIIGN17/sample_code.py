def greet_user(name):
    """Greets a user by name.

    Args:
        name (str): Name of the user.

    Returns:
        str: Greeting message.
    """
    return f"Hello, {name}!"

def add(a, b):
    """Adds two numbers and returns the result.

    Args:
        a (int): First number.
        b (int): Second number.

    Returns:
        int: Sum of a and b.
    """
    return a + b